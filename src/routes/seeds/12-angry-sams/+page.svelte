  <section class="intro">
    <p>Hello! As you can see from the watermark in your lower-right field of vision, you are currently a simulation. Thank you for joining us! Today we are prototyping a new form of consensual criminal justice.</p>

    <p>It probably feels less new to you. Remember how I told you you were a simulation? You're a future version of Sam — maybe you remember that name? Sorry if you don't use it anymore! — the you that's on trial right now.</p>

    <p>Together with other future-possible-yous, <strong>YOU</strong> will decide what crime you committed and <strong>YOU</strong> will decide what punishment you will receive.</p>

    <p class="thanks">Thank You, and Congratulations!</p>
  </section>

  <section>
    <h2>The Game</h2>
    <p>This is tabletop roleplaying game for 3–6 people over 2–4 hours. There is no consistent GM — players take turns setting up scenes and playing them out. To play you'll need some pencils, this book, one Present Sam sheet per GAME, one Future Sam sheet per PLAYER, and maybe some index cards and/or other scratch paper.</p>

    <p>You will also need an opaque bag and two colors of counters that feel the same. At the beginning of the game, put one clear token in for each player and one opaque token.</p>

    <p>Each player is taking the role of a simulated Juror in an experimental trial. Each player is taking the role of a future version of Sam, the person on trial. Together you will decided on their punishment and, to an extent, which one of you has a chance of existing.</p>

    <p class="aside">You can absolutely play it with more than six players. If you do, you should allow more time. I also super don't recommend doing that as everyone's first game. If you do run a full 12 player game, PLEASE let me know how it goes!</p>
  </section>

  <section>
    <h2>Setup</h2>

    <h3>Safety</h3>
    <p>This is a game about exploring crime, punishment, intent, and impact. It can be fun, serious, and anywhere around and between. It can be very easy for your game to unintentionally wander into someone's personal traumascape.</p>

    <p>Before you begin play, either break out your favorite safety tools or do the following:</p>

    <p>Take an index card, draw a big <strong>X</strong> on it, and put it in the middle of the table. During play, anyone can touch this card (or say 'X' or cross their arms in an X depending on your layout). If someone does this, everyone should drop the current scene or conversation, dip out, and move on. The person does not need to explain themselves — just imagine you realized something about the current scene didn't make sense after all and you had to edit it out. Leave room for the person to get up and spend some time away if they need to.</p>

    <p>Take a second index card and ask anyone if there are any topics they don't want to touch on. Also, especially for a game like this, I recommend going ahead and putting "Sexual Violence" right at the top of the list. Anyone should feel free to put anything that would ruin the game for them on it. Maybe you have severe arachnophobia. Maybe there are some words / slurs that pull you back to some really dark memories. Feel free to put them on the list.</p>

    <p>Ultimately, this is a game where you get to imagine what an ideal future might look like. You don't have to reproduce the problems of the present.</p>

    <h3>Determine The Charges</h3>
    <p>At the beginning of the game, you're going to decide who exactly Sam is and what exactly they are on trial for. Right now, you don't have to decide who Sam is, how old they are or even what their pronouns are. All those come later.</p>

    <p>As a group, pick <strong>ONE</strong> thing that Sam is charged with and fill it in on the character sheet. Maybe it's a familiar crime, like Vandalism. Maybe it's a weird future crime. The charges will very much determine the tone of your game. Once you reach consensus, write it down and move on to your own sheet.</p>

    <h3>Building Sams</h3>
    <p>After determining the charges, give every player a "Future Sam" sheet. Fill out the top part of the sheet individually and then do the bottom part after sharing out your details. The directions are printed on the sheet.</p>

    <p>After you have made all of your Future Sam sheets, you should as a group elect a foreperson. If one player is teaching the game to everyone else, they are probably the foreperson. Otherwise, you can determine who the foreperson will be by statements and a show of hands.</p>

    <p>The foreperson is responsible for making sure procedures are followed and for making decisions when group direction is unclear.</p>

    <p>After electing a Foreperson, fill in the rest of the info on the Sam sheet — legal name at time of trial, pronouns / name at time of trial, etc.</p>

    <p>Now, you are ready for the first scene!!</p>
  </section>

  <section>
    <h2>Playing the Game</h2>
    <p>For this game, you will take turns framing and playing in scenes that give some more detail about the life of Sam after the trial.</p>

    <p>When it is your turn to frame a scene (start to the LEFT of the Foreman and go clockwise) follow these steps:</p>

    <ol>
      <li>Decide on a question you want to answer. Examples are on the bottom of the character sheet.</li>
      <li>Decide who (besides Sam) is in the scene. Pick up to two people who are there — feel free to make people up or borrow people from other timelines who might be different here.</li>
      <li>Frame the location and timing of the scene.</li>
      <li>Anyone can say they want to play as one of the characters. If there is a dispute, foreperson decides.</li>
      <li>Everyone whose role was not named may choose to describe a character they will play in the scene, or may choose to <strong>OBSERVE</strong>.</li>
      <li>A SCENE ends when the current FRAMER decides that the question has been answered thoroughly.</li>
    </ol>

    <p>During the scene, Sam will describe things that they are trying to do. You might come to an impasse or the action may stall. Whenever any of these happens, any of the players can draw a stone from the bag and consult / narrate the results based on the notes on their character sheets.</p>
  </section>

  <section>
    <h2>Sentencing</h2>
    <p>After <strong>EACH</strong> player has framed a scene, all players should close their eyes and point at the Sam whose punishment they most want to see, then open their eyes.</p>

    <p>If there is <strong>consensus</strong>, the sentence has been handed down. Proceed to the epilogue.</p>

    <p>If there is <strong>no clear majority</strong>, do another round of scenes OR, if you are short of time, the player with the most votes is the determined punishment. Proceed to epilogue.</p>

    <p>If there is <strong>a majority</strong>, dissenting players may choose to frame scenes. If you have time, do another round of scenes, but this time skip the Majority player. Otherwise the player with the majority is the determined future. Proceed to epilogue.</p>
  </section>

  <section>
    <h2>Epilogue</h2>
    <p>After the sentence has been determined, you will go around twice. The first time you go around, describe how YOUR Sam spends their last hour of existence. The second time you go around, describe, in brief, one event in the future life of Present Sam.</p>
  </section>

